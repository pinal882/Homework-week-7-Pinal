package homeworkweek7;

public class Program_18SumOfArray {
    int x[] = {100,200,300,400,500,600};

    public static void main(String[] args) {
        int x[] = {100,200,300,400,500,600};
        int sum = 0;
        for (int i : x )
            sum += i;
        System.out.println(" The sum is " + sum);
    }

}
